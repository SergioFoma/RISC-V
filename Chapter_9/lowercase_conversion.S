#
# программа перевода входного строкового литерала в нижний регистр
#
# a1 - входящая строка (входные данные)
# a2 - выходная строка (выходные данные)
#

.include "stack_macros.S"

.global lowercase_conversion                            # делаем функцию видимой для линкера

.section .text
lowercase_conversion:
    push_1 ra                                           # сохраняем ra перед основным кодом
    
    mv t0, a2                                           # сохраням адрес начало выходной строки для расчета длины
    # Перевод в нижний регистр
    do_while_start:
    lb t2, 0(a1)                                        # загрузка первого символа по адресу input_str
    addi a1, a1, 1                                      # увеличиваем адрес выходной строки, так как текущий символ уже обработан
    li t3, 'A'                                          # загрузка символа 'A' в t3 для сравнения
    blt t2, t3, do_while_else                           # если исследуемый символ меньше 'A' - переход к else
    li t3, 'Z'                                          # загрузка символа 'Z' в t3 для сравнения
    blt t3, t2, do_while_else                           # если исследуемый символ больше 'Z' - переход в else
    
    main_cycle:
    addi t2, t2, ('a' - 'A')                            # исследуемый символ  - 'A' + 'a' 

    do_while_else:
    sb t2, 0(a2)                                        # загружаем символ в выходную строку
    addi a2, a2, 1                                      # увеличиваем адрес на 1, так как текущий символ мы уже обработали
    li t3, 0x0                                          # загружаем нулевой байт для сравнения
    bne t2, t3, do_while_start                          # если не конец строки - продолжаем перевод
    
    addi a2, a2, -1                                     # учли зря посчитанный \0
    sub a0, a2, t0                                      # сохраняем длину строки в a0
    pop_1 ra                                            # сохраняем адресс возврата
    ret


    
