#
# программа демонстрирующая работу с функциями и макросами
# Функция: перевод всех алфавитных символов в нижний регистр
# Макрос: вывод строкового литерала в стандартный поток вывода
# Макрос: добавление и удаление элементов из стека
#
# a1 - хранит адрес входной строки (сохраняемый регистр)
# a2 - хранит адрес выходной строки (сохраняемый регист)
# a0 - длина выходной строки (возвращаемое значение ихз функции)
#

.include "print.S"
.include "stack_macros.S"

.global _start

.section .rodata
.equ output_capacity, 255                               # емкость выходного строкового литерала
.equ repeat         , 1                                 # сколько раз нужно выделить необходимую емкость
.equ value          , 0                                 # значение по умолчанию для ининциализации

input_str: .asciz "HeLLo, PlAyErS!\n"                   # входная строка
second_str: .asciz "Str AgAin!!\n"

.section .data
output_str: .fill output_capacity, repeat, value        # выделение памяти под выходную стркоу

.text
_start:
    la a1, input_str                                    # загрузка в регистр a1 адреса входной строки
    la a2, output_str                                   # загрузка в регистр a2 адреса выходной строки
    
    push_2 a1, a2                                       # сохраняем a1, a2 в стек
    jal conversion                                      # вызваем функцию перевода в нижний регистр
    pop_2  a1, a2                                       # забираем адреса из стека
    print  a2, a0                                       # вызываем макрос печати строки на экран

    la a1, second_str                                   # загрузка в регистр a1 адреса новохой входной строки
    la a2, output_str                                   # загрузка в регистр a2 выходной строки

    push_2 a1, a2                                       # сохраняем адрес
    jal conversion                                      # вызваем функцию перевода в нижний регистр
    pop_2  a1, a2                                       # возвращаем старые адреса
    print  a2, a0                                       # вызваем макрос печати строки на экран
   
    # Завершение программы
    li a0, 0                                            # a0 = 0 - возращаемое значение
    li a7, 93                                           # a7 = 93 - системный вызов
    ecall                                               # вызов Linux


    
