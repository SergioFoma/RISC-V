#
# библиотека для работы с файлами
#

.section .rodata
.equ O_RDONLY      , 0                                       # только для чтения
.equ O_WRONLY      , 1                                       # только для записи
.equ O_RDWR        , 2                                       # для записи и чтения
.equ O_CREAT       , 64                                      # создать файл, если он отсутсвует
.equ O_APPEND      , 1024                                    # дозапись в конец файла
.equ O_TRUNC       , 512                                     # перезаписать файл, удалив его старое содержимое
.equ O_EXCL        , 128                                     # выдать ошибку, если файл уже существует (с O_CREAT)
.equ FULL_ACCESS   , 0777                                    # полный доступ для всех
.equ RWE_OWNER     , 0700                                    # чтение и запись для владельца
.equ STANDARD_MODE , 0644                                    # чтение и запись для владельца, чтение для остальных
.equ RW_OWNER      , 0600                                    # чтение и запись только для владельца

#
# открытие файла
# filename - путь к файлу (строковый литерал)
# flags - флаги работы с файлом (непосредственный операнд)
# access_rights - права доступа (непосредственный операнд)
# используются регистры: a0, a1, a2, a3 и a7
# ОШИБКА: a0 < 0
#
.MACRO file_open filename, flags, access_rights
       li a7, 56                                             # номер систменого вызова openat
       li a0, -100                                           # относительный путь отсчитывать от текущего каталога
       la a1, \filename                                      # загружаем адрес файла
       li a2, \flags                                         # флаги
       li a3, \access_rights                                 # права доступа
       ecall                                                 # вызов Linux
.ENDM

#
# запись в файл
# file_fd - файловый дескриптор (регистр)
# buffer - адресс, содержимое которого записывается в файл
# len - количество записываемых байт (регистр)
# используемые регистры: a0, a1, a2 и a7
# ОШИБКА: a0 < 0
#
.MACRO file_write file_fd, buffer, len
       li a7, 64                                             # номер системного вызова write
       mv a0, \file_fd                                       # передаем файловый дескриптор
       ld a1, \buffer                                        # загружаем буффер
       mv a2, \len                                           # передаем длину буффера
       ecall                                                 # вызов Linux
.ENDM 

#
# чтение из файла
# file_fd - файловый дескриптор (регистр)
# buffer - адресс, куда будут записаны считанные байты
# len - количество считываемых байт (регистр)
# используемые регистры: a0, a1, a2 и a7
#
.MACRO file_read file_fd, buffer, len
       li a7, 63                                            # номер системного вызова read
       ld a1, \buffer                                       # загрузка буффера, куда будет сохранено содержимое файла 
       mv a2, \len                                          # длина считываем данных в байтах
       mv a0, \file_fd                                      # передаем файловый дескриптор
       ecall                                                # вызов Linux
.ENDM

#
# закрытие файла
# file_fd - файловый дескриптор (регистр)
# используемые регистры: a0 и a7
# ОШИБКА: a0 < 0
#
.MACRO file_close file_fd
       li a7, 57                                             # номер системного вызова close
       mv a0, \file_fd                                       # передаем файловый дескриптор
       ecall                                                 # вызов Linux
.ENDM

#
# получение информации и файле
# fd_reg - файловый дескриптор (регистр)
# stat_buf - буфер для сохранения метаданных (адрес)
# используемы регистры: a0, a1 и a7
# ОШИБКА: a0 < 0
#
.MACRO file_stat fd_reg, stat_buf
       li a7, 80                                            # номер системного вызова
       mv a0, \fd_reg                                       # a0 = файловый дескриптор
       la a1, \stat_buf                                     # загружаем адрес буфера
       ecall

.ENDM

#
# получение размера файла
# reg -  регистр куда будер сохранена длина файла
# stat_buf - буфер с методанными 
# используемые регистры: t0
#
.MACRO get_file_size reg, stat_buf
       la t0, \stat_buf                                     # загружаем адрес буфера с методанными
       ld \reg, 48(t0)                                      # поле st_size находится со смещением 120 байт
.ENDM
