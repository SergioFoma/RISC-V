#
# библиотека для работы с памятью
#

.include "print.S"

.section .rodata
1:
log_error: .asciz "\nMemory allocation error\n"       # лог для сообщения об ошибке выделения памяти
2:
.equ LOG_LEN, 2b-1b                                   # длина лога

#
# выделение непроинициализированной памяти
# bytes - количество выделяемых байтов (кратно 4096 байт)
# используемые регистры: a0, a7, t0, t1, t2, s0
# в s0 хранится адрес начало выделенной памяти
# в случае ошибки программа завершается с соответсвующим логом
#
.MACRO malloc bytes
       li a7, 214                                     # номер системного вызова для sys_brk
       li a0, 0                                       # 0 - вернуть текущий адрес кучи (результат в a0)
       ecall                                          # вызов Linux 

       mv s0, a0                                      # s0 = a0
       li t0, \bytes                                  # t0 = bytes
       add a0, s0, t0                                 # новый адрес = старый адрес + 4096 байт
       mv t2, a0                                      # сохраняем потенциальный новый адрес

       li a7, 214                                     # систменый вызов для сдвига кучи
       ecall                                          # вызов Linux
       
       beq t2, a0, 1f                                 # если ноый адрес равен потенциальному -  пропускаем вызов ошибки
       
       la t0, log_error                               # загружаем log_error в t0
       li t1, LOG_LEN                                 # t1 = LOG_LEN
       print t0, t1
       li a7, 93                                      # систменый вызов для завершения программы
       li a0, 0                                       # код ошибки
       ecall                                          # вызов Linux
        
      1:
.ENDM
